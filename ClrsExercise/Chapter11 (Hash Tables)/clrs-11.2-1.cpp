/*
与生日悖论求期望的方式类似:
定义指示器随机变量X(i, j) = I{h(i) == h(j)}, 即表示两者碰撞, 为1; 否则为0.
那么, 总的碰撞数, 随机变量X = sum(i = 1 ... n) * sum(j = i + 1 ... n) * X(i, j).
两边求期望:
E(X) = E(sum(i = 1 ... n) * sum(j = i + 1 ... n) * X(i, j)).
     = sum(i = 1 ... n) * sum(j = i + 1 ... n) * E(X(i, j)) (期望的线性性质).
由于h(i) == h(j)的概率为1 / m, 因此E(X(i, j)) = 1 / m.
所以:
E(X) = sum(i = 1 ... n) * sum(j = i + 1 ... n) * 1 / m
     = n * (n - 1) / 2m.
*/