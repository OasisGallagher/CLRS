// 即: 大于x的最小的2的整数幂.
// 定义执行过第i次该方法之后的Φ为U(i)-i.
// 如果i是2的整数幂, 那么, 
// c^i = ci + Φ(i) - Φ(i-1) = ci + U(i) - i - U(i - 1) + (i - 1)
//     = ci + U(i) - U(i-1) - 1 = i + i - 1 = 2i - 1.
// 如果i不是2的整数幂, 那么:
